package es.api.docu.webapp.dtos;

public class Blog {

	
	private String title;
	private String date;
	
	public Blog() {
		
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	
}